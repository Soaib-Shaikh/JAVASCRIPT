// # Arithmetic Operators:
// + (Addition)
// - (Subtraction)
// * (Multiplication)
// / (Division)
// % (Modulus - remainder)
// ** (Exponentiation) -->

let a = 10;
let b = 5;

let sum = a + b; 
let subtract = a - b;
let multiplication = a * b;
let division = a / b;
let remainder = a % b;
let exponent = a ** b;

console.log("Sum:", sum); 
console.log("Difference:", subtract);
console.log("Multiplication:", multiplication);
console.log("Divison:", division);
console.log("Remainder:", remainder);
console.log("Exponent:", exponent);